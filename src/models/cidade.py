class Cidade:
    def __init__(self, id, nome, uf, totaldecasos):
        self.id = id
        self.nome = nome
        self.uf = uf
        self.totaldecasos = totaldecasos

    def add_total(self, qtde):
        self.totaldecasos += qtde

    def sub_total(self, qtde):
        self.totaldecasos -= qtde


    
    



    















        
    